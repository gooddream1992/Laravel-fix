<?php

namespace App;

use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;

class User extends Authenticatable
{
    use Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name','activation','serviceArea','whatsup','snapchat','instagram','follow_me','email_me','website','straight','hair','bust','personal_type','pet','drink','food','service','age','nationality','sexuality','eyes','bodyShape','escortType','escortTouring','serviceOffer','dress','height','price','phone','country','gender','city','state', 'code','roleStatus','photo', 'email', 'password',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password', 'remember_token',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];
}
